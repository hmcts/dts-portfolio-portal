#!/usr/bin/env node
//
// scripts/write-next-env.mjs — writes a minimal next-env.d.ts stub.
//
// Why: next-env.d.ts is generated and continuously rewritten by
// Next.js itself (the import path of `routes.d.ts` flips between
// `.next/types/routes.d.ts` (build) and `.next/dev/types/routes.d.ts`
// (dev) every time you swap between `next build` and `next dev`).
// create-next-app's default .gitignore excludes it for this reason.
//
// Our .gitignore now matches that convention, but we still need the
// file to exist before `tsc --noEmit` runs in CI (and on a fresh
// clone locally) — tsconfig.json's `"include"` lists it explicitly,
// and the broader src/ relies on the `<reference types="next" />`
// triple-slash directives for Next's ambient types.
//
// We deliberately OMIT the `import "./.next/.../routes.d.ts"` line
// — that file only exists after `next dev` or `next build` has run
// (so referencing it pre-build would fail). Next.js will add it
// back the first time it runs locally; the rewrite stays gitignored.

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const TARGET = resolve(ROOT, "next-env.d.ts");

const STUB = `/// <reference types="next" />
/// <reference types="next/image-types/global" />

// NOTE: This file is regenerated by Next.js on every \`next dev\` /
// \`next build\`. It is gitignored; see scripts/write-next-env.mjs.
`;

// Idempotent: only write if the file is missing OR has been
// stripped to nothing. If Next has already rewritten it with the
// routes import, leave it alone — that's the canonical content
// for whichever mode last ran.
if (!existsSync(TARGET)) {
  writeFileSync(TARGET, STUB);
  console.log("[write-next-env] wrote stub next-env.d.ts");
} else {
  const current = readFileSync(TARGET, "utf8");
  if (current.trim() === "") {
    writeFileSync(TARGET, STUB);
    console.log("[write-next-env] replaced empty next-env.d.ts with stub");
  } else {
    // Existing content (either a Next-generated rewrite or our
    // stub from a previous run). No-op.
  }
}
